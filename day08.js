/*
These instructions are visited in this order:

nop +0  | 1
acc +1  | 2, 8(!)
jmp +4  | 3
acc +3  | 6
jmp -3  | 7
acc -99 |
acc +1  | 4
jmp -4  | 5
acc +6  |
First, the nop +0 does nothing. Then, the accumulator is increased from 0 to 1 (acc +1) and jmp +4 sets the next instruction to the other acc +1 near the bottom. After it increases the accumulator from 1 to 2, jmp -4 executes, setting the next instruction to the only acc +3. It sets the accumulator to 5, and jmp -3 causes the program to continue back at the first acc +1.

This is an infinite loop: with this sequence of jumps, the program will run forever. The moment the program tries to run any instruction a second time, you know it will never terminate.

Immediately before the program would run an instruction a second time, the value in the accumulator is 5.

Run your copy of the boot code. Immediately before any instruction is executed a second time, what value is in the accumulator?
*/

/* a program listing of [command, (int)arg] pairs */;
function parseInput(input) {
  return input.map(line => { 
    var [op, arg] = line.split(' ');
    return [op, parseInt(arg)];
    });
}

function makeProgram(listing) {
  return /* object with accumulator, program counter, and step function */ {
    accumulator: 0,
    pc: 0,
    step: function() {
      switch (listing[this.pc][0]) {
        case 'acc':
          this.accumulator += listing[this.pc][1];
          // fall through
        case 'nop':
          this.pc++;
          break;
        case 'jmp':
          this.pc += listing[this.pc][1];
      }
      return this;
    },
    listingLength: listing.length,
  };
}

function findLoopAccumulator(program) {
  var executed = new Set();
  while (!executed.has(program.pc) && program.pc >= 0 && program.pc < program.listingLength) {
    executed.add(program.pc);
    program.step();
  }
  return program.accumulator;
}

console.assert(findLoopAccumulator(makeProgram(parseInput(day08test))) === 5);

